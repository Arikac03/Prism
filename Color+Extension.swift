import SwiftUI

extension Color {
    static let spotlightTheme = SpotlightTheme()
}

struct SpotlightTheme {
    let primary = Color("SpotlightPurple") // #4C4F7F
    let secondary = Color("SpotlightLavender") // #EBDBFF
    let accent = Color("SpotlightLightBlue") // #E5F8FE
    let background = Color("SpotlightOffWhite") // #F9F8ED
    let text = Color("SpotlightDarkGray") // #464643
} 